class DictionaryEntry2 {
  private String word;
  private String definition;

  public DictionaryEntry2(String word, String definition) {
      this.word = word;
      this.definition = definition;
  }

  public String getWord() {
      return word;
  }

  public String getDefinition() {
      return definition;
  }
}